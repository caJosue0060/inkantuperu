export function gql(strings, ...args) {
  let str = "";
  strings.forEach((string, i) => {
    str += string + (args[i] || "");
  });
  return str;
}
export const ToursPartsFragmentDoc = gql`
    fragment ToursParts on Tours {
  __typename
  title
  short_description
  main_image
  duration
  altitude
  level
  price
  itinerary {
    __typename
    day
    title
    description
  }
  body
}
    `;
export const PagesPartsFragmentDoc = gql`
    fragment PagesParts on Pages {
  __typename
  title
  body
}
    `;
export const ToursDocument = gql`
    query tours($relativePath: String!) {
  tours(relativePath: $relativePath) {
    ... on Document {
      _sys {
        filename
        basename
        hasReferences
        breadcrumbs
        path
        relativePath
        extension
      }
      id
    }
    ...ToursParts
  }
}
    ${ToursPartsFragmentDoc}`;
